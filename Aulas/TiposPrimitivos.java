package Aulas;

public class TiposPrimitivos {
    public static void main(String[] args) {
        // Tipos primitivos em Java
        // byte, short, int, long, float, double, char, boolean

        // Exemplo de declaração e inicialização de variáveis
        byte b = 1; // Números inteiros pequenos
        short s = 10000; // Números inteiros médios
        int idade = 21; // Números inteiros no geral
        long l = 100000L; // Números inteiros grandes

        float f = 10.5f; // Números decimais simples
        double d = 20.99; // Números decimais precisos

        char c = 'A'; // Um único caractere
        boolean bool = true; // Verdadeiro ou falso

        // Exibindo os valores das variáveis
        System.out.println("Byte: " + b);
        System.out.println("Short: " + s);
        System.out.println("Int: " + idade + " anos");
        System.out.println("Long: " + l);
        System.out.println("Float: " + f);
        System.out.println("Double: " + d);
        System.out.println("Char: " + c);
        System.out.println("Boolean: " + bool); 
        
    }
}